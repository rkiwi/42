int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb > 2147395600)
		return (0);
	while (i * i != nb)
	{
		i++;
		if ((nb == 0) || (i > nb))
		{
			return (0);
		}
	}
	return (i);
}
