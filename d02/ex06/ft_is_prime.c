int		ft_is_prime(int nb)
{
	int i;
	int j;

	i = 3;
	if (nb % 2 == 0)
		return (0);
	j = nb / 2;
	while ((i < (j / 2) && (j > (j / 2))))
	{
		if (((nb % i) == 0) || ((nb % j) == 0))
			return (0);
		i = i + 2;
		j = j - 2;
	}
	return (1);
}
