int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int i;

	i = 0;
	while ((s1[i] != '\0' || s2[i]) != '\0' && n != 0)
	{
		if (s1[i] == s2[i])
		{
			i++;
			continue;
		}
		else if (s1[i] < s2[i])
		{
			return (-1);
		}
		else
		{
			return (1);
		}
		n--;
	}
	return (0);
}
